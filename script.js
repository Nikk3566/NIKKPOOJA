const showLetter = (index) => {
  const letter = letters[index];
  letterTitle.textContent = letter.title;
  letterSubtitle.textContent = letter.subtitle;
  letterContent.innerHTML = letter.lines.map(line => `<p>${line}</p>`).join('');
  proposalLine.textContent = letter.proposal;
  shayariOne.innerHTML = letter.shayari1.map(line => `<p>${line}</p>`).join('');
  shayariTwo.innerHTML = letter.shayari2.map(line => `<p>${line}</p>`).join('');
  romanticLink.textContent = letter.linkText;
  loveLetter.classList.remove('hidden');
};

letterCards.forEach((card) => {
  card.addEventListener('click', () => {
    const index = Number(card.dataset.index);
    showLetter(index);
  });
});

const animateCards = () => {
  letterCards.forEach((card, index) => {
    card.style.transform = `translateY(${Math.sin(Date.now() / 1000 + index) * 6}px)`;
  });
};

setInterval(animateCards, 180);
